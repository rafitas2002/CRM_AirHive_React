-- DB-first CRM audit log (append-only) for reliable historical analysis.
-- Captures lead, meeting and forecast snapshot changes with before/after payloads.
-- This table is independent from crm_events to avoid breaking existing dashboards.

CREATE TABLE IF NOT EXISTS public.crm_audit_log (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    event_type TEXT NOT NULL,
    entity_type TEXT NOT NULL,
    entity_id TEXT NOT NULL,
    actor_user_id UUID NULL REFERENCES public.profiles(id) ON DELETE SET NULL,
    event_source TEXT NOT NULL DEFAULT 'db_trigger',
    before_data JSONB NULL,
    after_data JSONB NULL,
    context_data JSONB NOT NULL DEFAULT '{}'::jsonb,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_crm_audit_log_created_at
    ON public.crm_audit_log (created_at DESC);

CREATE INDEX IF NOT EXISTS idx_crm_audit_log_type_created_at
    ON public.crm_audit_log (event_type, created_at DESC);

CREATE INDEX IF NOT EXISTS idx_crm_audit_log_entity
    ON public.crm_audit_log (entity_type, entity_id, created_at DESC);

CREATE INDEX IF NOT EXISTS idx_crm_audit_log_actor_created_at
    ON public.crm_audit_log (actor_user_id, created_at DESC);

CREATE INDEX IF NOT EXISTS idx_crm_audit_log_context_gin
    ON public.crm_audit_log USING GIN (context_data);

CREATE INDEX IF NOT EXISTS idx_crm_audit_log_before_gin
    ON public.crm_audit_log USING GIN (before_data);

CREATE INDEX IF NOT EXISTS idx_crm_audit_log_after_gin
    ON public.crm_audit_log USING GIN (after_data);

ALTER TABLE public.crm_audit_log ENABLE ROW LEVEL SECURITY;

DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM pg_policies
        WHERE schemaname = 'public'
          AND tablename = 'crm_audit_log'
          AND policyname = 'crm_audit_log_service_role_all'
    ) THEN
        CREATE POLICY crm_audit_log_service_role_all
            ON public.crm_audit_log
            FOR ALL
            TO service_role
            USING (true)
            WITH CHECK (true);
    END IF;
END $$;

CREATE OR REPLACE FUNCTION public.write_crm_audit_event(
    p_event_type TEXT,
    p_entity_type TEXT,
    p_entity_id TEXT,
    p_actor_user_id UUID DEFAULT NULL,
    p_before_data JSONB DEFAULT NULL,
    p_after_data JSONB DEFAULT NULL,
    p_context_data JSONB DEFAULT '{}'::jsonb,
    p_event_source TEXT DEFAULT 'db_trigger'
)
RETURNS VOID
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
    IF NULLIF(TRIM(COALESCE(p_event_type, '')), '') IS NULL THEN
        RETURN;
    END IF;
    IF NULLIF(TRIM(COALESCE(p_entity_type, '')), '') IS NULL THEN
        RETURN;
    END IF;
    IF NULLIF(TRIM(COALESCE(p_entity_id, '')), '') IS NULL THEN
        RETURN;
    END IF;

    INSERT INTO public.crm_audit_log (
        event_type,
        entity_type,
        entity_id,
        actor_user_id,
        event_source,
        before_data,
        after_data,
        context_data
    )
    VALUES (
        TRIM(p_event_type),
        TRIM(p_entity_type),
        TRIM(p_entity_id),
        p_actor_user_id,
        COALESCE(NULLIF(TRIM(COALESCE(p_event_source, '')), ''), 'db_trigger'),
        p_before_data,
        p_after_data,
        COALESCE(p_context_data, '{}'::jsonb)
    );
EXCEPTION
    WHEN OTHERS THEN
        -- Keep business flows resilient; audit failures should not block writes.
        RAISE LOG 'write_crm_audit_event failed: %', SQLERRM;
END;
$$;

